numbers = [3, 1, 4, 1, 5, 9, 2]

print(numbers[0])
# will print the first element

print(numbers[-1])
# will print the last element

print(numbers[3])
# will print the 4th element

print(numbers[:-1])
# will print all element except last element

print(numbers[3:4])
# will print in between elements 2 and 3

print(5 in numbers)
# will print True

print(7 in numbers)
# will print False

print("3" in numbers)
# will print False

print(numbers + [6, 5, 3])
